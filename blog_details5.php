                        <h2>
                        Tribal Museum - An Ode to Tribal Art and Culture
                        </h2>
                        <ul class="blog-info-link mt-3 mb-4">
                           <li><a href="#"><i class="fa fa-user"></i> Travel, Wildlife</a></li>
                           <li><a href="#"><i class="fa fa-comments"></i> 03 Comments</a></li>
                        </ul>
                        <p class="excert">
                        There are several museums in the city of lakes which have a lot of unheard truth of bygone era in its folds and Bhopal's diversity is clearly reflected in its museums. One of the biggest attractions here is Tribal museum. A tour to this museum just once is sure to make you a repeat visitor for all the unique presentations once can witness here.
  
                        </p>
 
                        <p>
                        The biggest attraction of tribal museum are its well planned and beautifully placed themed galleries where every visitor is able to connect via motifs and artefacts representing tribal lifestyle. The museum indeed stands out from rest of the museums because of the way its exhibits are put up.
                        </p>
                         <p>
                         It does not limits to the old, sedate style of showing individual artworks as distinct masterpieces, with little contextual information. Instead, it celebrates the ways of life practiced by the seven major tribes in the state - the Gond, Bhil, Korku, Baiga, Sahariya, Kol and Bhariya - through their crafts. In a series of colorful galleries whose roofs soar as high as three-storey buildings, the dramatically-lit artefacts project tribal living, aesthetics, and spiritual beliefs, one is sure to fall in love with the museum as in whole.


                        </p>

                        <p>
                           <b>What's inside the museum?</b>
                        </p>
                        <p><i>A gallery of homes:</i></p>
                        <p>
                        Retaining the authenticity and richness of the tribal culture and traditions, this gallery showcases the homes of the tribes-Gond,Korku, Bhil, Sahariya, while highlighting their dependency on natural resources. The houses in the gallery are made up of mud, bamboo, dung, hay, grass also portraying the essentials such as agricultural tools, earthenware used by the people from tribal communities.
                        </p>

                        <p><i>Cultural diversity well preserved:</i></p>
                        <p>
                        Another gallery at the museum literally celebrates different traditions associated with weddings and festivals of the tribal communities. The piousness associated with each natural resource like forests, flowers, leaves, farms with sculptures of men and women have been brought to life by artists. Significance of a 'vivah mandap' at the centre, musical instruments like Dholak and Damru hanging from the revered banyan, and relevance of other rituals, have been colorfully portrayed. Additionally, the mythical relevance of bamboo originating from the story of 'Baasin Kanya' (Gond folklore) is narrated with much fervor and beauty.
                        </p>
                        <p><i>Devlok- The house of gods:</i></p>
                        <p>
                        The entire spectrum of different myths and beliefs associated with customs of worshipping mother earth, mountains, rivers, etc. have been recreated. A huge banyan tree at one corner, different kinds of terracotta artefacts painted with white and red are realistic creations, here at the Devlok.
                        </p>
                        <p><i>The story of how games originated:</i></p>
                        <p>
                        The moment you step inside this gallery you relive your childhood! Age old games like 'Ghoda Badam Shahi', the traditional tug of war and more will take you back in times when these games were played outside every household. The sculptures of men, women and specially children playing these games are created in a way that one gets the real feel of these games. The section underscores the usage of natural materials for fun and leisurely pursuits.
                        </p>


                        <div class="quote-wrapper">
                           <div class="quotes">
                             <b>Things you should know about Tribal museum of Bhopal:</b> 
                             <ol class="ordered-list">
									<li><span>The strikingly beautiful building of tribal museum is being designed by Revathi Kamath, renowned Indian architect.</span></li>
									<li><span>The colorful museum is being divided into six different galleries showcasing various tribes of Madhya Pradesh.</span></li>
									<li><span>  This museum is literally a home that is well preserving the state's rich art and culture bestowed in various tribes. The folklores, lifestyle, rituals and social customs of all seven indigenous tribes of MP?the Gonds, Bhils, Bharias, Sahariya, Korku, Kol, and Baiga are showcased in the museum through a repository of traditional art. In fact, lives of the region's seven tribes are being celebrated here via their craft.</span>
									<li><span>The moment you step into the galleries inside the museum, you get a strong feel about not just witnessing the  galleries but a strong feeling of entering straight into the lifestyles of the tribes of Madhya Pradesh. </span></li>
                                    <li><span> Alongside taking a look at the beautifully designed galleries in the museum one can also stop at an amphitheater inside the museum which hosts plays from time to time for theater buffs. The schedule of the plays, musical performances, cultural dances can be obtained from the museum itself.</span></li>
                                    <li><span> A shop 'Chinhari' in the museum is also a must visit. It is basically a retail outlet for handicraft connoisseurs and -exhibits a huge collection of beautiful artefacts all available for sale at reasonable price.</span></li>
                                    <li><span>          One can even take a look at the 'Choumasa' Magazine editions, an in house magazine of the museum with detailed information on the tribal lifestyle.</span></li>
								</ol>
                                This extraordinary museum captures the unfettered imagination of the indigenous people in the state. It is a space in which life stories have been told and shared with the world in imaginative ways.


