									<li><span>Follow the dress code in case you wish to offer prayers at the inner sanctum.  Men should wear a dhoti and women need to wear a Saree. </span></li>
									<li><span>The aarti is extremely popular as it is attended by large numbers of devotees therefore prior booking is required.</span>
									<li><span>Online bookings are available. A month in advance booking is recommended. </span></li>
									<li><span>Mobiles or cameras are not allowed inside the temple premises. Devotees need to deposit gadgets inside the locker room.</span></li>
								</ol>
 

 Do visit Ujjain and soak in this powerful, soul-stirring experience at Mahakaleshwar Jyotirlinga.  
