                        <h2>
                            MP is now the Proud Gharial State of India 
                        </h2>
                        <ul class="blog-info-link mt-3 mb-4">
                           <li><a href="#"><i class="fa fa-user"></i> Travel, Wildlife</a></li>
                           <li><a href="#"><i class="fa fa-comments"></i> 03 Comments</a></li>
                        </ul>
                        <p class="excert">
                        With the largest forest cover and maximum national parks in India, Madhya Pradesh is a naturally blessed state that has plenty of offerings for its people and tourists. After being declared India's tiger state last year, the state has recently added another feather to its cap by becoming the state with the highest count of Gharial. The continuous efforts for the conservation of wild creatures by the forest officials are now bearing fruits.  
                        </p>
 
                        <p>
                         Gharials are the long-snouted fish-eating endangered crocodilians which live in medium flowing rivers. They leave the water only for basking and making the nest on high sandbanks. Chambal Gharial Sanctuary provides the longest viable habitat to this species as the Chambal River is one of the cleanest rivers in India with abundant prey base and least threats. It hosts incredible species of freshwater turtles, crocodiles (such as mugger, gharial), riverine mammals (such as Gangetic River dolphin and Smooth Coated Otters) and variety of aquatic birds.
                        </p>
                         <p>
                         Total 9 types of turtles are found in this river system, out of which Red-Crowned Roof turtle is only found in Chambal River in the world, as the population got extirpated from the entire range. The adjoining environs of the river turn into a paradise for bird watchers during winters (Nov-Mar) as one can see thousands of aquatic birds flock at the shores of the river. 
                        </p>

                        <p>
                            Forest department has been putting sincere efforts since 2008-09 to stabilise the population of Gharial through the Deori Eco Centre in Morena which was built for the conservation of the species. One gharial can lay up to 35-95 eggs in the late spring season (Mar-Apr) and collectively there are thousands of eggs laid each season but most of them vanish due to environmental factors.
                        </p>
                        <p>
                        That's when the main contributor Deori Eco Centre comes into the role, where around 200 eggs are collected from the sandbanks and are artificially incubated and hatched. The hatchlings are raised for two years or 1 meter. With such viable approaches, the population of gharial has today reached 2000 approx. making Madhya Pradesh the proud 'Gharial state' of India.
                        </p>

